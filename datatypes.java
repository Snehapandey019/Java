class datatypes{
    public static void main(String[] args) {
        byte a =34;
        System.out.println(a);

        short b =45;
        System.out.println(b);

        int c =123;
        System.out.println(c);

        float d = 12.34f;
        System.out.println(d);

        double e = 456789.1235456;
        System.out.println(e);

        char f = 'A';
        System.out.println(f);
        
 }
}