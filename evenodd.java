//Check whether a number is even or odd
class evenodd{
    public static void main(String[] args) {
        int n = 10;
        if (n % 2 == 0) {
            System.out.println(n + " is even");
        } else {
            System.out.println(n + " is odd");
        }
        int y = 5;
        if (y % 2 == 0) {
            System.out.println(y + " is even");
        } else {
            System.out.println(y + " is odd");
        }
    }
}